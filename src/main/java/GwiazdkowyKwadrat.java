

public class GwiazdkowyKwadrat {
    public GwiazdkowyKwadrat() {
    }

    public static void main(String[] args) {
        int i;
        for(i = 1; i <= 3; ++i) {
            System.out.print("* ");
        }

        System.out.println();
        System.out.print("* ");

        for(i = 1; i <= 1; ++i) {
            System.out.print("  ");
        }

        System.out.println("* ");

        for(i = 1; i <= 3; ++i) {
            System.out.print("* ");
        }

    }
}